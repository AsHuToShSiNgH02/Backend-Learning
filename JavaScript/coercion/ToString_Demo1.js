console.log("Ashutosh" + "Singh");
/**
 * Both of them is already a string,
 * so to primitive conversion will also be a string
 * we concatenate them
 */
console.log(undefined + "node");
/**
 * undefined -> "undefined"
 * "undefined" + "node" = "undefinednode"
 */
console.log("5" + null);
/**
 * One of them is already a string, so both of them becomes a string
 * "5" + "null" -> "5null"
 */
console.log("Ashutosh" + true);
/**
 * true -> "true"
 * "Ashutosh" + "true" = "Ashutoshtrue"
 */
